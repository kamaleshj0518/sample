name = "kamalesh"
print(name)
print(name[0]) # prints first character
print(name[7]) # prints eighth character
print(name[-1]) # prints last character
print(name[-8]) # prints first character
print(name[0:4]) # prints first four characters
print(name[4:]) # prints from fifth character to end
print(name[:4]) # prints first four characters
print(name[::2]) # prints every second character
print(name[::-1]) # prints the string in reverse order
